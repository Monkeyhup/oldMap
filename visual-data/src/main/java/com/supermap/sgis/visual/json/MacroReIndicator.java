package com.supermap.sgis.visual.json;

import java.util.ArrayList;
import java.util.List;

/**
 * 综合查询指标结果项 纳入分组
 *
 * @author Created by W.Qiong on 14-11-11.
 */
public class MacroReIndicator {

    /** 指标编码 */
    public String idenCode;

    /** 指标名称 */
    public String idenName;

    /** 指标单位 */
    public String idenUnit;

    /** 指标类型 */
    public int idenType;

    /** 分组枚举类ID */
    public int groupId;

    /** 子指标项 */
    public List<MacroReIndicator> subs = new ArrayList<>();

    /** 报告期时段 */
    public List<Period> periods = new ArrayList<>();

    /**
     * 取得指标编码
     *
     * @return 指标编码
     */
    public String getIdenCode() {
        return idenCode;
    }

    /**
     * 设置指标编码
     *
     * @param idenCode
     *            指标编码
     */
    public void setIdenCode(String idenCode) {
        this.idenCode = idenCode;
    }

    /**
     * 取得指标名称
     *
     * @return 指标名称
     */
    public String getIdenName() {
        return idenName;
    }

    /**
     * 设置指标名称
     *
     * @param idenName
     *            指标名称
     */
    public void setIdenName(String idenName) {
        this.idenName = idenName;
    }

    /**
     * 取得指标单位
     *
     * @return 指标单位
     */
    public String getIdenUnit() {
        return idenUnit;
    }

    /**
     * 设置指标单位
     *
     * @param idenUnit
     *            指标单位
     */
    public void setIdenUnit(String idenUnit) {
        this.idenUnit = idenUnit;
    }

    /**
     * 取得指标单位
     *
     * @return 指标单位
     */
    public int getIdenType() {
        return idenType;
    }

    /**
     * 设置指标单位
     *
     * @param idenType
     *            指标单位
     */
    public void setIdenType(int idenType) {
        this.idenType = idenType;
    }

    /**
     * 取得分组枚举类ID
     *
     * @return 分组枚举类ID
     */
    public int getGroupId() {
        return groupId;
    }

    /**
     * 设置分组枚举类ID
     *
     * @param groupId
     *            分组枚举类ID
     */
    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }

    /**
     * 取得子指标项
     *
     * @return 子指标项
     */
    public List<MacroReIndicator> getSubs() {
        return subs;
    }

    /**
     * 设置子指标项
     *
     * @param subs
     *            子指标项
     */
    public void setSubs(List<MacroReIndicator> subs) {
        this.subs = subs;
    }

    /**
     * 取得报告期时段
     *
     * @return 报告期时段
     */
    public List<Period> getPeriods() {
        return periods;
    }

    /**
     * 设置报告期时段
     *
     * @param periods
     *            报告期时段
     */
    public void setPeriods(List<Period> periods) {
        this.periods = periods;
    }

}
