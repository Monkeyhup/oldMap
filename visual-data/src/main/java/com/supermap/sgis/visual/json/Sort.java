package com.supermap.sgis.visual.json;

/**
 * 排序对象
 * <p>
 * 说明：<br/>
 * 1.基层&综合公用<br/>
 * 2.用户检索数据时的排序
 * </p>
 *
 * @author Created by W.Qiong on 14-2-25.
 */
public class Sort {
    /** 排序字段（数据库字段） */
    private String property;

    /** 排序字段（数据库字段）名称 */
    private String propertyName;

    /** 排序方式（ASC后排名、DESC前排名）,默认为 ASC(升序) */
    private String direction = "ASC";

    /** 取结果最大记录数，默认为1000000000 */
    private int top = 1000000000;

    /** 无参构造函数 */
    public Sort() {
    }

    /**
     * 取得排序字段（数据库字段）
     * @return 排序字段（数据库字段）
     */
    public String getProperty() {
        return property;
    }

    /**
     * 设置排序字段（数据库字段）
     * @param property
     * 			排序字段（数据库字段）
     */
    public void setProperty(String property) {
        this.property = property;
    }

    /**
     * 取得排序字段（数据库字段）名称
     * @return 排序字段（数据库字段）名称
     */
    public String getPropertyName() {
        return propertyName;
    }

    /**
     * 设置排序字段（数据库字段）名称
     * @param propertyName
     * 			排序字段（数据库字段）名称
     */
    public void setPropertyName(String propertyName) {
        this.propertyName = propertyName;
    }

    /**
     * 取得排序方式（ASC后排名、DESC前排名）
     *
     * @return 排序方式（ASC后排名、DESC前排名）
     */
    public String getDirection() {
        return direction;
    }

    /**
     * 设置排序方式（ASC后排名、DESC前排名）
     * @param direction
     * 			排序方式（ASC后排名、DESC前排名）
     */
    public void setDirection(String direction) {
        this.direction = direction;
    }

    /**
     * 取得取结果最大记录数
     * @return 取结果最大记录数
     */
    public int getTop() {
        return top;
    }

    /**
     * 设置取结果最大记录数
     * @param top
     * 			取结果最大记录数
     */
    public void setTop(int top) {
        this.top = top;
    }
}
